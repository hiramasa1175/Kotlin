fun main(args: Array<String>) {
  // + - * / %
  val x = 10
  println(x / 3)
  println(x / 3.0)
  println(x % 3)

  // ++ --
  var y = 5
  y++
  println(y)
  y--
  println(y)

  var z = 4
  // z = z + 12
  z += 16
  println(z)

  // AND OR NOT
  // && || !
  val flag = true
  println(!flag)
}